const mongoose = require("mongoose");

const admin = mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        default: "admin"
    }
})

const Admin = mongoose.model("Admin", admin)
module.exports = Admin